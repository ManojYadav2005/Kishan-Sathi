// ── Language Engine ──
const Lang = (() => {
  function getCurrent() {
    return localStorage.getItem('ks_lang') || 'en';
  }
  function set(lang) {
    localStorage.setItem('ks_lang', lang);
    // also update on server if logged in
    const token = localStorage.getItem('ks_token');
    if (token) {
      fetch('/api/auth/profile', {
        method: 'PATCH',
        headers: { 'Content-Type':'application/json', 'Authorization':'Bearer '+token },
        body: JSON.stringify({ lang })
      }).catch(() => {});
    }
    apply(lang);
    window.location.reload();
  }
  function t(key) {
    const lang = getCurrent();
    return (i18n[lang] && i18n[lang][key]) || (i18n.en[key]) || key;
  }
  function apply(lang) {
    // Update all [data-i18n] elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = (i18n[lang] && i18n[lang][key]) || (i18n.en[key]) || key;
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = val;
      else if (key.endsWith('_html')) el.innerHTML = val;
      else el.textContent = val;
    });
    // Update [data-i18n-ph] for placeholders
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      const key = el.getAttribute('data-i18n-ph');
      el.placeholder = (i18n[lang] && i18n[lang][key]) || (i18n.en[key]) || key;
    });
    // Update html lang attribute
    document.documentElement.lang = lang === 'hi' ? 'hi' : 'en';
    // Update body font for Hindi
    if (lang === 'hi') document.body.classList.add('lang-hi');
    else document.body.classList.remove('lang-hi');
    // Update toggle button text
    document.querySelectorAll('.lang-toggle-btn').forEach(btn => {
      btn.textContent = lang === 'hi' ? i18n.hi.lang_switch : i18n.en.lang_switch;
    });
  }
  function init() {
    const lang = getCurrent();
    apply(lang);
  }
  return { getCurrent, set, t, apply, init };
})();

// Auto-init when DOM ready
if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', () => Lang.init());
else Lang.init();
